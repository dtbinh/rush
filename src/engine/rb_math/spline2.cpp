Vec2 Hermite( const Vec2& p0, const Vec2& t0, const Vec2& p1, const Vec2& t1, float t )
{
    Vec2 res = (2.0f*(p0 - p1) + t1 + t0)*t*t*t + 
               (3.0f*(p1 - p0) - 2.0f*t0 - t1)*t*t + t0*t + p0;
    return res;
}